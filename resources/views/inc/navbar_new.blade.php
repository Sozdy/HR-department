    <script>
        function menuIconSwitch() {
            document.querySelector('.menu-icon').classList.toggle('menu-icon-active');
            document.querySelector('.nav-list-drop').classList.toggle('visible');
        }


        $(document).ready(() => {
            $(".search-button").click(search);
            $(".search-close").click(() => {
                $(".search-container").css('display', 'none');
                isSearchStarted = false;
            });

            $(".search-field").on("change paste keyup", function(e) {
                if (e.keyCode == 13)
                    search(e);

                $(".search-field").val($(this).val());
            });

            let isSearchStarted = false;

            function search(e) {
                e.preventDefault();

                $(".search-field")[0].focus();

                if (!isSearchStarted) {
                    $(".search-container").css('display', 'flex');

                    isSearchStarted = true;
                } else {
                    $(".gsc-input[name='search']").val($(".search-field").val());
                    $(".gsc-search-button-v2").click();

                    $(".search-container").css('display', 'none');
                    isSearchStarted = false;
                }
            }

        });

        $(document).scroll((e) => {
            $(".middle-logo-container").css("height", (7 - $(document).scrollTop()/70) + "vw");

             if($(document).scrollTop() > 100)
                 $(".middle-logo-container").css("box-shadow", "0 0 50px rgba(0, 0, 0, 0.3)");
             else
                 $(".middle-logo-container").css("box-shadow", "");
        });
    </script>
@endpush
